# doubly linked list

class Node:
  def __init__(self, data):
    self.data = data
    self.next = None
    self.prev = None


class DoublyLinkedList:
  def __init__(self):
     self.head = None

  def insert_at_begin(self, data):
    new_node = Node(data)
    new_node.next = self.head

    if self.head:
      self.head.prev = new_node

    self.head = new_node

  def insert_at_end(self, data):
    new_node = Node(data)
    if not self.head:
      self.head = new_node
      return

    last = self.head
    while last.next:
      last = last.next

    last.next = new_node
    new_node.prev = last


  def display_froward(self):
    print("froward:", end=" ")

    temp = self.head
    while temp:
      print(temp.data, end=" -> ")
      last = temp
      temp = temp.next
    print("None")

  def display_reverse(self):
    print("reverse:", end=" ")

    temp = self.head
    if not temp:
      print("empty")
      return
    while temp.next:
      temp = temp.next

    while temp:
      print(temp.data, end=" -> ")
      temp = temp.prev
    print("None")

