# singly linked list
class Node:
  def __init__(self, data):
    self.data = data
    self.next = None


class linkedlist:
  def __init__(self):
     self.head = None

  def insert_at_begin(self, data):
    new_node = Node(data)
    new_node.next = self.head
    self.head = new_node

  def insert_at_end(self, data):
    new_node = Node(data)
    if not self.head:
      self.head = new_node
      return

    last = self.head
    while last.next:
      last = last.next

    last.next = new_node

  def display(self):
    temp = self.head

    while temp:
      print(temp.data, end=" -> ")
      temp = temp.next
    print("None")

