def add (x, y):
    return x + y
def subtract (x, y):
    return x-y
def multiply (x, y):
    return x*y
def divide (x, y):
    if y!= 0:
        return x/y
    else:
      print("Error: Division by zero")
      return None
def modules(x, y):
    return x%y
def square(x):
    return x**2
def square_root(x):
    if x < 0:
        print("Error: Cannot calculate square root of a negative number")
        return None
    else:
        return x**0.5
def power(x, y):
    return x**y
def absolute_value(x):
    return abs(x)
def factorial(x):
  if x < 0:
        print("Error: Factorial is not defined for negative numbers")
        return None
  else:
    if x == 0 or x == 1:
        return 1
    else:
       result=x*factorial(x-1)
